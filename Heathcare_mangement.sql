 -- RFM segmentatiom
 -- recency , frequency, monetary
 use healthcare_mng_casestudy ;
 select max(cost) from treatments;
 -- create RFM metrices	per patients using :- last visit, total visit, paid spend
 -- label "champions", 'loyal high value', 'risk'
 with rfm as (
 select p.patient_id, concat(p.first_name, p.last_name) as patient_name,
 max(a.appointment_date) last_visit, count(distinct a.appointment_id) as frequency,
 round(coalesce (sum(case when b.payment_status = 'Paid' then b.amount end),0),2) as monetary from patients p left join appointments a
 on p.patient_id = a.patient_id left join billing b 
 on p.patient_id=b.patient_id group by p.patient_id, patient_name
 ),
 scored as (
 select *, datediff(curdate(), last_visit) as recency_days,
 ntile(4) over (order by datediff(curdate(), last_visit) asc) as r_score , -- lower receny better
 ntile(4) over (order by frequency desc) as f_score,
 ntile(4) over (order by monetary desc) as m_score from rfm)
 select patient_id, patient_name, recency_days, frequency,monetary,
 r_score, f_score, m_score, concat(r_score,f_score,m_score) as rfm_code,
 case when r_score >=3 and f_score >=3 and m_score>=3 then "Champions"
 when f_score >=3 and m_score >=3 then "Loyal High Value"
 when r_score <=2 and f_score <=2 then "High risk/ Inactive"
 when f_score >=3 then "Frequent visitors"
 when m_score >=3 then "High spenders"
 else "Regular" end as Segment from scored order by monetary desc, frequency desc;

