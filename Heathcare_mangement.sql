-- make age distribution 
-- 18-35 = Adult
-- 36-55 = Mature
-- 56+ = Old
with Age as (Select patient_id,timestampdiff(year, date_of_birth, curdate()) as age1 from patients)
select 
case when age1 <18 then "Minors" else(
	case when age1 between 18 and 35 then 'Adult' else 
		(case when age1 between 36 and 55 then 'Mature' else "Old_age" end)         /* Nested case statement*/
	end )
end as age_group, count(patient_id) as Patient_count from age group by age_group order by Patient_count desc ;

