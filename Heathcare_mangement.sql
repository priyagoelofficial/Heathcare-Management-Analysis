-- which patients are repeatedly missing appointment and may need inetrvention
-- appoitment>3 and no -show rate > 40
with patient_summary as  (select p.patient_id, concat(p.first_name, p.last_name) as patient_name,
count(appointment_id) as total_appointment,
sum(case when a.status = "No-show" then 1 else 0 end) as no_show_count,
round(sum(case when a.status = "No-show" then 1 else 0 end)* 100 / count(appointment_id),2) as no_show_rate,
count(appointment_id)- sum(case when a.status = "No-show" then 1 else 0 end) as meet_appointment
from patients p join  appointments a on p.patient_id=a.patient_id group by p.patient_id, patient_name)
select * from patient_summary order by no_show_count desc;

