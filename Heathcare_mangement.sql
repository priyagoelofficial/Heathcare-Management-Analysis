			when age3.age1 < 18 then 'Minors'
			when age3.age1 between 18 and 35 then "Adults"
            when age3.age1 between 36 and 55 then "Mature"
			else 'Old_age' 
    end as Age_group, count(patient_id) as patient_count from age3 
    group by age_group order by patient_count desc;         /* I have used View to not write again & again timestamp query, 
    we can use multiple when statement in single case statement instead of nested case statement */
    
    
